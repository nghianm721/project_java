    public boolean kiemtra() {
        //kiem tra rong
        String id = txtmasv.getText();
        String ten = txthoten.getText();
        String email = txtemail.getText();
        String sdt = txtsdt.getText();
        String dc = txtdiachi.getText();
        //kiem tra rong
        if (id.length() == 0) {
            JOptionPane.showConfirmDialog(this, "Bạn chưa nhập MSSV!", "TB", JOptionPane.CLOSED_OPTION);
            txtmasv.requestFocus();
            txtmasv.setBackground(Color.red);
            return false;
        }
        if (ten.length() == 0) {
            JOptionPane.showConfirmDialog(this, "Bạn chưa nhập Tên sinh viên !", "TB", JOptionPane.CLOSED_OPTION);
            txthoten.requestFocus();
            txthoten.setBackground(Color.red);
            return false;
        }
        if (email.length() == 0) {
            JOptionPane.showConfirmDialog(this, "Bạn chưa nhập địa chỉ email !", "TB", JOptionPane.CLOSED_OPTION);
            txtemail.requestFocus();
            txtemail.setBackground(Color.red);
            return false;
        }
        if (sdt.length() == 0) {
            JOptionPane.showConfirmDialog(this, "Bạn chưa nhập số điện thoại !", "TB", JOptionPane.CLOSED_OPTION);
            txtsdt.requestFocus();
            txtsdt.setBackground(Color.red);
            return false;
        }
        if (dc.length() == 0) {
            JOptionPane.showConfirmDialog(this, "Bạn chưa nhập địa chỉ !", "TB", JOptionPane.CLOSED_OPTION);
            txtdiachi.requestFocus();
            txtdiachi.setBackground(Color.red);
            return false;
        }
        //ktra giới tính bị trốn
        if (!rdnam.isSelected() && !rdnu.isSelected()) {
            JOptionPane.showConfirmDialog(this, "Bạn chưa nhập Giới Tính !", "TB", JOptionPane.CLOSED_OPTION);
            rdnam.setBackground(Color.red);
            rdnu.setBackground(Color.red);
            return false;
        }
        //kiem tra mail
        // String rex = "^\\w+[a-z0-9]*@{1}\\w+mail.com$";
        String rex = "^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$";
        if (!email.matches(rex)) {
            txtemail.requestFocus();
            JOptionPane.showConfirmDialog(this, "Không đúng định dạng  email", "TB", JOptionPane.CLOSED_OPTION);
            txtemail.requestFocus();
            txtemail.setBackground(Color.red);
            tbemail.setText("email phải có dạng ...@...mail.com");
            return false;
        }
        //kiem tra sdt
        String Rexsdt = "^0+\\d{9}";
        if (!sdt.matches(Rexsdt)) {
            JOptionPane.showConfirmDialog(this, "SĐT Bạn nhập không hợp lệ phải đủ 10 số!", "TB", JOptionPane.CLOSED_OPTION);
            txtsdt.requestFocus();
            txtsdt.setBackground(Color.red);
            tbsdt.setText("bắt đầu từ số 0, đủ 10 số");
            return false;
        }
        //kiem tra mssv
        String Rexmssv = "\\d{8}";
        if (!id.matches(Rexmssv)) {
            JOptionPane.showConfirmDialog(this, "Mssv Bạn nhập không hợp lệ pahi3 đủ 8 số !", "TB", JOptionPane.CLOSED_OPTION);
            txtmasv.requestFocus();
            txtmasv.setBackground(Color.red);
            tbmssv.setText("mssv phải đủ 8 số");
            return false;
        }
        return true;
    }

    public SinhVien laydulieu() {
        SinhVien sv = new SinhVien();
        lop g = new lop();
        g.setMalop(txtmalop.getText());
        sv.setLop(g);
        sv.setMasv(txtmasv.getText());
        sv.setTensv(txthoten.getText());
        boolean gt = false;
        if (rdnam.isSelected()) {
            gt = true;
        }
        sv.setGioitinh(gt);
        sv.setSdt(txtsdt.getText());
        sv.setEmail(txtemail.getText());
        sv.setDiachi(txtdiachi.getText());
        return sv;
    }

