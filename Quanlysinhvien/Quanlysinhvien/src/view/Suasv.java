 public void setEditData(SinhVien s) {
        txtmalop.setText(s.getLop().getMalop());
        txtmalop.setEditable(false);
        txtmasv.setText(s.getMasv());
        txtmasv.setEditable(false);
        txthoten.setText(s.getTensv());
        String sex = s.isGioitinh() ? "Nam" : "Nữ";
        if (sex.equals("Nam")) {
            rdnam.setSelected(true);
        } else {
            rdnu.setSelected(true);
        }
        txtsdt.setText(s.getSdt());
        txtemail.setText(s.getEmail());
        txtdiachi.setText(s.getDiachi());
    }
