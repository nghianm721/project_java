    public void laydulieu() {
        txtnguoigui1.setText(UserDAO.nguoidangnhap.getEmail());
        listdiem.clear();
        listdiem = diemdao.laydanhsachdiem();
        model = (DefaultTableModel) tbdiem.getModel();
        model.setRowCount(0);
        for (grade g : listdiem) {
            Object[] rowData = new Object[7];
            rowData[0] = g.getStudent().getMasv();
            rowData[1] = g.getStudent().getTensv();
            rowData[2] = g.getToan();
            rowData[3] = g.getJava();
            rowData[4] = g.getTiengAnh();
            String tb = String.format("%.2f", g.getDiemtb());
            rowData[5] = tb;
            rowData[6] = g.getXeploai();
            model.addRow(rowData);
        }
    }

