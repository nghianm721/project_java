    private void tbdiemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbdiemMouseClicked
        // TODO add your handling code here:   
        model = (DefaultTableModel) tbdiem.getModel();
        txttimkiem.setText(model.getValueAt(tbdiem.getSelectedRow(), 0).toString());
        try {
            int row = tbdiem.getSelectedRow();
            if (row >= 0) {
                String idsv = (String) tbdiem.getValueAt(row, 0);
                SinhVienDAO dao = new SinhVienDAO();
                SinhVien sv = dao.laysvkhongcanlop(idsv);
                String idbd = (String) tbdiem.getValueAt(row, 0);
                gradeDAO bddao = new gradeDAO();
                grade bd = bddao.getgradebymasv(idbd);
                String tb = String.format("%.2f", bd.getDiemtb());
                if (sv != null || bd != null) {
                    txtnguoinhan1.setText(sv.getEmail());
                    txtnoidung1.setText("MSSV: " + sv.getMasv()
                            + "\nHọ Tên SV: " + sv.getTensv()
                            + "\nĐiểm Java: " + bd.getJava()
                            + "\nĐiểm Toán: " + bd.getToan()
                            + "\nĐiểm Tiếng Anh: " + bd.getTiengAnh()
                            + "\nĐiểm TB: " + tb
                            + "\nHọc lực: " + bd.getXeploai()
                            + "\nNếu có sai sót gì báo cáo giảng viên ngay !!");
                }
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_tbdiemMouseClicked

