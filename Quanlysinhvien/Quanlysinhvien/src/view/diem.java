    private void btndtxuatexcelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndtxuatexcelActionPerformed
        int dong = tbdiem.getRowCount();
        if (dong <= 0) {
            return;
        }
        try {
            JFileChooser savefile = new JFileChooser();
            int result = savefile.showSaveDialog(btndtxuatexcel);
            if (result != JFileChooser.APPROVE_OPTION) {
                return;
            }
            File fileToSave = savefile.getCurrentDirectory();
            String link = fileToSave.getPath();
            String ten = savefile.getSelectedFile().getName();
            String linksave = link + "\\" + ten + ".xls";

            WritableWorkbook workbook = Workbook.createWorkbook(new File(linksave));
            WritableSheet sheet = workbook.createSheet("BẢNG ĐIỂM SINH VIÊN ", 0);
            sheet.addCell(new Label(0, 0, "Mã Sinh Viên"));
            sheet.addCell(new Label(1, 0, "Tên Sinh Viên"));
            sheet.addCell(new Label(2, 0, "Điểm Toán"));
            sheet.addCell(new Label(3, 0, "Điểm Java"));
            sheet.addCell(new Label(4, 0, "Điểm Tiếng Anh"));
            sheet.addCell(new Label(5, 0, "Điểm TB"));
            sheet.addCell(new Label(6, 0, "Xếp Loại"));

            for (int i = 1; i <= dong; i++) {
                sheet.addCell(new Label(0, i, tbdiem.getValueAt(i - 1, 0).toString()));
                sheet.addCell(new Label(1, i, tbdiem.getValueAt(i - 1, 1).toString()));
                sheet.addCell(new Label(2, i, tbdiem.getValueAt(i - 1, 2).toString()));
                sheet.addCell(new Label(3, i, tbdiem.getValueAt(i - 1, 3).toString()));
                sheet.addCell(new Label(4, i, tbdiem.getValueAt(i - 1, 4).toString()));
                sheet.addCell(new Label(5, i, tbdiem.getValueAt(i - 1, 5).toString()));
                sheet.addCell(new Label(6, i, tbdiem.getValueAt(i - 1, 6).toString()));

            }
            workbook.write();
            workbook.close();
            JOptionPane.showMessageDialog(this, "Xuất file thành công!!!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi: " + e);
        }

    }//GEN-LAST:event_btndtxuatexcelActionPerformed

    private void btnguemailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguemailActionPerformed
        // TODO add your handling code here:
        tbdiemMouseClicked(null);
        Properties prop = new Properties();
        prop.put("mail.smtp.host", "smtp.gmail.com");
        prop.put("mail.smtp.port", "587");
        prop.put("mail.smtp.auth", "true");
        prop.put("mail.smtp.starttls.enable", "true"); //TLS
        Session session = Session.getInstance(prop,
                new javax.mail.Authenticator() {
            protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication(txtnguoigui1.getText(), new String(txtpassword1.getPassword()));
            }
        });
        try {
            MimeMessage message = new MimeMessage(session);
            message.setFrom(new InternetAddress(txtnguoigui1.getText()));
            message.setRecipients(
                    Message.RecipientType.TO,
                    InternetAddress.parse(txtnguoinhan1.getText())
            );
            message.setSubject(txttieude1.getText());
            message.setText(txtnoidung1.getText());

            Transport.send(message);
            JOptionPane.showMessageDialog(this, "gửi thành công");
            System.out.println("Done");

        } catch (MessagingException e) {
            e.printStackTrace();
        }
        //}
    }//GEN-LAST:event_btnguemailActionPerformed

