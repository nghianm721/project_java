    public boolean kiemtra() {
        //kiem tra rong
        String java = txtjava.getText();
        String tienganh = txttienganh.getText();
        String toan = txttoan.getText();

        if (java.length() == 0) {
            JOptionPane.showConfirmDialog(this, "Bạn chưa nhập điểm java!", "TB", JOptionPane.CLOSED_OPTION);
            txtjava.requestFocus();
            txtjava.setBackground(Color.red);
            return false;
        }
        if (tienganh.length() == 0) {
            JOptionPane.showConfirmDialog(this, "Bạn chưa nhập điểm tiếng anh !", "TB", JOptionPane.CLOSED_OPTION);
            txttienganh.requestFocus();
            txttienganh.setBackground(Color.red);
            return false;
        }
        if (toan.length() == 0 || toan.length() > 10) {
            JOptionPane.showConfirmDialog(this, "Bạn chưa nhập điểm toán hoặc lớn hơn 10 !", "TB", JOptionPane.CLOSED_OPTION);
            txttoan.requestFocus();
            txttoan.setBackground(Color.red);
            return false;
        }
        //dieu kiện nhập diem 0 den 10
        if (Float.parseFloat(txtjava.getText()) > 10 || Float.parseFloat(txtjava.getText()) < 0) {
            JOptionPane.showConfirmDialog(this, "Chỉ được nhập từ 0 ->10!", "TB", JOptionPane.CLOSED_OPTION);
            txtjava.requestFocus();
            txtjava.setBackground(Color.red);
            return false;
        }
        if (Float.parseFloat(txttoan.getText()) > 10 || Float.parseFloat(txttoan.getText()) < 0) {
            JOptionPane.showConfirmDialog(this, "Chỉ được nhập từ 0 ->10!", "TB", JOptionPane.CLOSED_OPTION);
            txttoan.requestFocus();
            txttoan.setBackground(Color.red);
            return false;
        }
        if (Float.parseFloat(txttienganh.getText()) > 10 || Float.parseFloat(txttienganh.getText()) < 0) {
            JOptionPane.showConfirmDialog(this, "Chỉ được nhập từ 0 ->10!", "TB", JOptionPane.CLOSED_OPTION);
            txttienganh.requestFocus();
            txttienganh.setBackground(Color.red);
            return false;
        }
        return true;
    }

    public grade laydulieu() {
        grade g = new grade();
        g.setId(0);
        SinhVien sv = new SinhVien(txtmasv.getText(), txthoten.getText());
        g.setStudent(sv);
        g.setToan(Double.parseDouble(txttoan.getText()));
        g.setJava(Double.parseDouble(txtjava.getText()));
        g.setTiengAnh(Double.parseDouble(txttienganh.getText()));
        return g;
    }

    private void btnluuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnluuActionPerformed
        // TODO add your handling code here:
        if (kiemtra()) {
            grade g = laydulieu();
            if (diemdao.getgradebymasv(g.getStudent().getMasv()) != null) {
                JOptionPane.showMessageDialog(this, "MSSV đã có điểm. Hãy nhập mssv khác");
            } else if (diemdao.themdiem(g) > 0) {
                JOptionPane.showMessageDialog(this, "Thêm thông tin thành công");
            }
        }
    }//GEN-LAST:event_btnluuActionPerformed
    public void setModel(grade g) {
        txttoan.setText(String.valueOf(g.getToan()));
        txtjava.setText(String.valueOf(g.getJava()));
        txttienganh.setText(String.valueOf(g.getTiengAnh()));
        txtmasv.setText(g.getStudent().getMasv());
        txthoten.setText(g.getStudent().getTensv());
        String tb = String.format("%.2f", g.getDiemtb());
        lbldiemtb.setText(tb);
    }
    private void btntimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntimkiemActionPerformed
        // TODO add your handling code here:
        String ma = (String) cbbmasv.getSelectedItem();
        if (ma.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập mã sinh viên tìm kiếm");
        } else {
            SinhVien sv1 = sinhviendao.laysvtheomasv(ma);
            if (sv1 != null) {
                grade d = diemdao.getgradebymasv(sv1.getMasv());
                if (d != null) {
                    setModel(d);
                } else {
                    txtmasv.setText(sv1.getMasv());
                    txthoten.setText(sv1.getTensv());
                    txttoan.setText("0.0");
                    txtjava.setText("0.0");
                    txttienganh.setText("0.0");
                    lbldiemtb.setText("0.0");
                }
            }
        }
    }//GEN-LAST:event_btntimkiemActionPerformed

