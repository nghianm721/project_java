    public void loadcombobox() {
        Connection con = database.DattabaseUtils.getDBConnect();
        String sSQL = " select *  FROM SinhVien";
        try {
            PreparedStatement sttm = con.prepareStatement(sSQL);
            ResultSet rs = sttm.executeQuery();
            while (rs.next()) {
                this.cbbmasv.addItem(rs.getString("masv"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

